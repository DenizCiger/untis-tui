#[derive(Clone, Copy)]
struct LessonMeta<'a> {
    cell_state: &'a str,
    teacher: &'a str,
    teacher_long_name: &'a str,
    room: &'a str,
    room_long_name: &'a str,
    classes: &'a [&'a str],
    lesson_text: &'a str,
    remarks: &'a str,
    cancelled: bool,
    substitution: bool,
}

fn period(name: &str, start: &str, end: &str) -> TimeUnit {
    TimeUnit {
        name: name.to_owned(),
        start_time: start.to_owned(),
        end_time: end.to_owned(),
    }
}

fn lesson(
    instance_id: &str,
    subject: &str,
    subject_long_name: &str,
    start_time: &str,
    end_time: &str,
    meta: LessonMeta<'_>,
) -> ParsedLesson {
    ParsedLesson {
        instance_id: instance_id.to_owned(),
        subject: subject.to_owned(),
        subject_long_name: subject_long_name.to_owned(),
        lesson_text: meta.lesson_text.to_owned(),
        cell_state: meta.cell_state.to_owned(),
        teacher: meta.teacher.to_owned(),
        teacher_long_name: meta.teacher_long_name.to_owned(),
        all_teachers: vec![meta.teacher.to_owned()],
        all_teacher_long_names: vec![meta.teacher_long_name.to_owned()],
        room: meta.room.to_owned(),
        room_long_name: meta.room_long_name.to_owned(),
        all_classes: meta
            .classes
            .iter()
            .map(|class| (*class).to_owned())
            .collect(),
        start_time: start_time.to_owned(),
        end_time: end_time.to_owned(),
        cancelled: meta.cancelled,
        substitution: meta.substitution,
        remarks: meta.remarks.to_owned(),
    }
}

fn absence(
    id: i64,
    start_date: NaiveDate,
    end_date: NaiveDate,
    start_time: &str,
    end_time: &str,
    is_excused: bool,
    excuse_status: &str,
    reason: &str,
    text: &str,
) -> ParsedAbsence {
    ParsedAbsence {
        id,
        student_name: "Portfolio Demo".to_owned(),
        reason: reason.to_owned(),
        text: text.to_owned(),
        excuse_status: excuse_status.to_owned(),
        is_excused,
        start_date,
        end_date,
        start_time: start_time.to_owned(),
        end_time: end_time.to_owned(),
    }
}

