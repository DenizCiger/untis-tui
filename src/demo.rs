pub fn demo_week_timetable(week_date: NaiveDate, target: &TimetableTarget) -> WeekTimetable {
    let monday = get_monday(week_date);
    let timegrid = demo_timegrid();
    let lessons_by_day = match target {
        TimetableTarget::Own => own_week_lessons(),
        TimetableTarget::Class { id, .. } if *id == DEMO_CLASS_ID => class_week_lessons(),
        TimetableTarget::Room { id, .. } if *id == DEMO_ROOM_ID => room_week_lessons(),
        TimetableTarget::Teacher { id, .. } if *id == DEMO_TEACHER_ID => teacher_week_lessons(),
        _ => own_week_lessons(),
    };

    let days = lessons_by_day
        .into_iter()
        .enumerate()
        .map(|(offset, lessons)| {
            let date = add_days(monday, offset as i64);
            DayTimetable {
                date,
                day_name: get_weekday_name(date),
                lessons,
            }
        })
        .collect();

    WeekTimetable { days, timegrid }
}

